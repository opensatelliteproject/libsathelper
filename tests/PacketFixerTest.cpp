    bool PacketFixerTest::RunTest() {
        PacketFixer packetFixer;
        bool result = false;
        uint8_t *data = new uint8_t[TEST_SIZE];

        std::cout << "  | Testing 0 degrees shift." << std::endl;
        std::memcpy(data, baseData, TEST_SIZE);
        packetFixer.fixPacket(data, TEST_SIZE, PhaseShift::DEG_0, false);
        result = std::memcmp(data, baseData, TEST_SIZE) == 0;
        if (!result) {
            std::cerr << "    - 0 degrees packet fixer failed." << std::endl;
            return result;
        } else {
            std::cout << "    + 0 degrees packet fixer OK." << std::endl;
        }

        std::cout << "  | Testing 90 degrees shift." << std::endl;
        std::memcpy(data, deg90, TEST_SIZE);
        packetFixer.fixPacket(data, TEST_SIZE, PhaseShift::DEG_90, false);
        result = std::memcmp(data, baseData, TEST_SIZE) == 0;
        if (!result) {
            std::cerr << "    - 90 degrees packet fixer failed." << std::endl;
            return result;
        } else {
            std::cout << "    + 90 degrees packet fixer OK." << std::endl;
        }

        std::cout << "  | Testing 180 degrees shift." << std::endl;
        std::memcpy(data, deg180, TEST_SIZE);
        packetFixer.fixPacket(data, TEST_SIZE, PhaseShift::DEG_180, false);
        result = std::memcmp(data, baseData, TEST_SIZE) == 0;
        if (!result) {
            std::cerr << "    - 180 degrees packet fixer failed." << std::endl;
            return result;
        } else {
            std::cout << "    + 180 degrees packet fixer OK." << std::endl;
        }

        std::cout << "  | Testing 270 degrees shift." << std::endl;
        std::memcpy(data, deg270, TEST_SIZE);
        packetFixer.fixPacket(data, TEST_SIZE, PhaseShift::DEG_270, false);
        result = std::memcmp(data, baseData, TEST_SIZE) == 0;
        if (!result) {
            std::cerr << "    - 270 degrees packet fixer failed." << std::endl;
            return result;
        } else {
            std::cout << "    + 270 degrees packet fixer OK." << std::endl;
        }

        std::cout << "  | Testing IQ Inversion." << std::endl;
        std::memcpy(data, degIQ, TEST_SIZE);
        packetFixer.fixPacket(data, TEST_SIZE, PhaseShift::DEG_0, true);
        result &= std::memcmp(data, baseData, TEST_SIZE) == 0;
        if (!result) {
            std::cerr << "    - IQ Inversion packet fixer failed." << std::endl;
            return result;
        } else {
            std::cout << "    + IQ Inversion packet fixer OK." << std::endl;
        }

        delete[] data;
        return result;
    }

